// Generate ~55K price reports across Algeria
function generatePriceReports(): PriceReport[] {
  const reports: PriceReport[] = []
  let id = 1

  // For each city, generate reports (~54,900 total)
  algeriaCities.forEach((city) => {
    // Generate 945-950 reports per city
    const reportsPerCity = 945 + Math.floor(Math.random() * 6)

    for (let i = 0; i < reportsPerCity; i++) {
      const product = productTemplates[Math.floor(Math.random() * productTemplates.length)]
      const priceVariation = 0.7 + Math.random() * 0.6 // 70% to 130% of average price
      const price = Math.round(product.averagePrice * priceVariation)
      const isAbnormal = priceVariation > 1.25 || priceVariation < 0.75

      // Add some randomness to position within the city
      const latOffset = (Math.random() - 0.5) * 0.1
      const lngOffset = (Math.random() - 0.5) * 0.1

      const commune = city.communes[Math.floor(Math.random() * city.communes.length)]

      // Generate random timestamp within last 30 days
      const daysAgo = Math.floor(Math.random() * 30)
      const timestamp = new Date()
      timestamp.setDate(timestamp.getDate() - daysAgo)
      timestamp.setHours(Math.floor(Math.random() * 24))
      timestamp.setMinutes(Math.floor(Math.random() * 60))

      reports.push({
        id: `price-${id++}`,
        productName: product.name,
        price,
        city: city.name,
        commune,
        reporterName: getRandomReporterName(),
        reporterId: `user-${Math.floor(Math.random() * 500) + 1}`,
        timestamp,
        latitude: city.latitude + latOffset,
        longitude: city.longitude + lngOffset,
        isAbnormal,
        upvotes: Math.floor(Math.random() * 50),
        downvotes: Math.floor(Math.random() * 10),
        category: product.category,
      })
    }
  })

  return reports.sort((a, b) => b.timestamp.getTime() - a.timestamp.getTime())
}

